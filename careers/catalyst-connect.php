    const swiper = new Swiper('.swiper', {
        // Optional parameters
        direction: 'horizontal',
        slidesPerView: 1.5, // Show 1 full slide + partial next
        spaceBetween: 50,   // Adjust spacing between slides
        centeredSlides: false, // Ensures the first slide starts from the left

        loop: false,
        // slidesperView: 1.5,
        // spaceBetween: 10,
        // freeMode: true,

        // If we need pagination

        // Navigation arrows
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev',
        },
    });

    const counters = document.querySelectorAll('.number');
    const speed = 200; // Adjust for speed of animation

    counters.forEach(counter => {
        const updateCounter = () => {
            const target = +counter.getAttribute('data-target');
            const current = +counter.innerText.replace(/[^0-9]/g, ""); // Remove non-numeric characters

            const increment = Math.ceil(target / speed);

            if (current < target) {
                const nextValue = Math.min(current + increment, target);
                const format = counter.getAttribute('data-format');

                // Update number with format
                if (format === "+") {
                    counter.innerText = `${nextValue.toLocaleString('en-IN')}+`;
                } else if (format === "₹Cr") {
                    counter.innerText = `₹${nextValue.toLocaleString('en-IN')} Cr`;
                }

                setTimeout(updateCounter, 10);
            } else {
                // Final value to ensure correct format
                const format = counter.getAttribute('data-format');
                if (format === "+") {
                    counter.innerText = `${target.toLocaleString('en-IN')}+`;
                } else if (format === "₹Cr") {
                    counter.innerText = `₹${target.toLocaleString('en-IN')} Cr`;
                }
            }
        };

        updateCounter();
    });

</script>



